public class MultiDArray {
    public static void main(String[] args) {
        int[][] a = {
                {1,2,3},
                {4,5,6,7},
                {8,9},
        };
        //length of array
        System.out.println("Row 1 length:" + a[0].length);
        System.out.println("Row 2 length:" + a[1].length);
        System.out.println("Row 3 length:" + a[2].length);

    }
}
